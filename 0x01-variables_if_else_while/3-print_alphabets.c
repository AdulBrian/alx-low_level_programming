#include <stdio.h>
/**
*description- print the alphabet in small letters and caps afterward
*print upper caps alphabets
*print new line
*/ 

int main(void)
{
char ch;
for (ch = 'a'; ch <= 'z'; ch++)
{
putchar(ch);
}
for (ch = 'A'; ch <= 'Z'; ch++)
putchar(ch);
putchar('\n');
return (0);
}



















